
public class Main {
    public static void main(String[] args) {
        // przykład pętli for
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        // przykład pętli while

        int i = 0;
        while (i < 10) {
            System.out.println("Hello World");
            i++;
        }

    }
}



